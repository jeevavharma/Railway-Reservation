    private void txtticketsKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtticketsKeyPressed
        // TODO add your handling code here:
        int price, ticketno, total;
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            price = Integer.parseInt(txtprice.getText());
            ticketno = Integer.parseInt(txttickets.getText());
            total = price * ticketno;
            txttotal.setText(String.valueOf(total));

        }
    }//GEN-LAST:event_txtticketsKeyPressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
       String pno = txtpassangerno.getText();
       String startplace = txtsplace.getSelectedItem().toString();
       String endplace = txtendplace.getSelectedItem().toString();
       String trainno = txttrainno.getText();
       String trainname = txttrainname.getText();
       String price = txtprice.getText();
       String date = txtdate.getText();
       String tickets = txttickets.getText();
       String total = txttotal.getText();
       
        try {
            pst = con.prepareStatement("insert into reservation(pno, startplace, endplace, trainno, trainname, price, date, tickets, total)values(?,?,?,?,?,?,?,?.?)");
            pst.setString(1, pno);
            pst.setString(2, startplace);
            pst.setString(3, endplace);
            pst.setString(4, trainno);
            pst.setString(5, trainname);
            pst.setString(6, price);
            pst.setString(7, date);
            pst.setString(8, tickets);
            pst.setString(9, total);
            int k = pst.executeUpdate();
            
            if(k == 1)
            {
                JOptionPane.showMessageDialog(this, "Record added");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Record failed");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(reservation.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

